/**
 * Enumeration representing the four cardinal directions in the game grid.
 * This enum is used to define movement directions for players and door orientations in rooms.
 */
public enum Direction {
    /** Represents the north direction. */
    NORTH,
    /** Represents the east direction. */
    EAST,
    /** Represents the south direction. */
    SOUTH,
    /** Represents the west direction. */
    WEST;
}
